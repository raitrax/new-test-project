Citizen.CreateThread(function()
   while true do
    Citizen.Wait(1)
    if IsVehicleModel(GetVehiclePedIsIn(GetPlayerPed(-1), true), GetHashKey("police")) then
       SetVehicleEnginePowerMultiplier(GetVehiclePedIsIn(GetPlayerPed(-1), true), 3.2) -- Accélération
       SetVehicleEngineTorqueMultiplier(GetVehiclePedIsIn(GetPlayerPed(-1), true), 3.5) -- Vitesse
     end
   end
 end)


